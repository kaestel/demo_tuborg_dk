			<h1>Tubklub medlemsbetingelser</h1>
			<p>Du skal være fyldt 18 år for at være medlem.</p>
			<p>Ved indmeldelse giver du hermed din udtrykkelige tilladelse til, at TubKlub kan anvende og behandle dine afgivne personoplysninger.</p>
			<p>Kun Tuborg må fremsende SMS/MMS til det afgivne mobilnummer, hvis denne service er tilvalgt, såfremt du ikke har accepteret andre specifikke afsendere.</p>
			<p>Kun Tuborg må fremsende e-mails til den afgivne adresse, såfremt du ikke har accepteret andre specifikke afsendere.</p>
			<p>Oplysninger afgivet i forbindelse med medlemskabet vil kun blive anvendt til brug for fremsendelse af materiale og tilbud forbundet med TubKlub samt intern statistisk behandling af TubKlubs medlemmer.</p>
			<p>Tuborg forbeholder sig ret til at indgå aftaler med 3. part i forbindelse med aktiviteter, der vedrører TubKlub. TubKlub vil dog ikke videregive dine personoplysninger til 3. part uden særskilt tilladelse fra dig i hvert enkelt tilfælde.</p>
			<p>Ved din udmeldelse af TubKlub, vil Tuborg slette dine oplysninger.</p>
			<p>I øvrigt henvises der til Persondataloven, som TubKlub overholder i dens helhed i forbindelse med indsamling og behandling af personoplysninger.</p>

